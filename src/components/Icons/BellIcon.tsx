import React from 'react';
import { IconProps } from './index';

/**
 * 铃铛图标组件
 * 用于通知、公告等
 * 引用 public/icons/ui/bell.svg 文件
 */
export const BellIcon: React.FC<IconProps> = ({
  size = 24,
  color = 'currentColor',
  className = '',
  style = {},
  onClick,
  'aria-hidden': ariaHidden = true,
  title,
  ...props
}) => {
  const iconStyle: React.CSSProperties = {
    width: size,
    height: size,
    color: color,
    display: 'inline-block',
    verticalAlign: 'middle',
    flexShrink: 0,
    ...style,
  };

  return (
    <img
      src="/icons/ui/bell.svg"
      alt={title || "通知"}
      loading="lazy"
      decoding="async"
      className={className}
      style={iconStyle}
      onClick={onClick}
      aria-hidden={ariaHidden}
      {...props}
    />
  );
};